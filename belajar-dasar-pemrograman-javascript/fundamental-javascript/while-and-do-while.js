//while dan do-while adalah dua jenis struktur pengulangan (loop) dalam pemrograman JavaScript (dan banyak bahasa pemrograman lainnya) yang memungkinkan Anda untuk menjalankan blok kode berulang kali selama kondisi tertentu terpenuhi.

let i = 1;

do {
    console.log(i);
    i++;
} while (i <= 100);