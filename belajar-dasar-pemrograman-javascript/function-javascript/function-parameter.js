// Parameter dalam JavaScript adalah nilai yang diterima oleh sebuah fungsi saat fungsi tersebut dipanggil.

// Contoh fungsi greet dengan parameter 'name'
function greet(name) {
    console.log(`Hello, ${name}!`);
}

// Memanggil fungsi greet dengan argumen "John"
greet("John"); // Output: Hello, John!

